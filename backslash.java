import javax.sql.rowset.spi.SyncResolver;

public class backslash {
    public static void main(String[] args) {
        System.out.println("\' this is single quote");
        System.out.println("\" this is double quote");
        System.out.println("\\ this is backslash ");
        System.out.println("this is \n new line ");
        System.out.println("this is \r carriage returns");
        System.out.println("this is \t tab");
        System.out.println("this is \b backspace");
        System.out.println("this is \f formfeed");
    }

}
